## The functions cache the matrix and solve for its inverse. 

##First apply makeCacheMatrix and then apply cache solve to cache the inverse of the matrix to 
##speed up processing speed. 

makeCacheMatrix <- function(x = matrix()) {
        m <- NULL
        set <- function(y) {
                x <<- y
                m <<- NULL
        }
        get <- function() x
        setinverse <- function(inv) m <<- inv
        getinverse <- function() m
        list(set = set, get = get,
             setinverse = setinverse,
             getinverse = getinverse)

}



